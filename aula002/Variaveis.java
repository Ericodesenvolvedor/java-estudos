public class Variaveis {
    public static void main(String[] args) {
        String nome = "Eric";
        int idade = 19;
        int quantosAnos = 10;
        int idadeNoFuturo = idade + quantosAnos;
        System.out.println("Olá " + nome + ", seja bem vindo");
        System.out.print("Daqui a " + quantosAnos + " Você vai ter: " + idadeNoFuturo);
    }
}